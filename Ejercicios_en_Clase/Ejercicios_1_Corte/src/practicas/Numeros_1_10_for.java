public class Numeros_1_10_for {
      public static void main(String[] args) {
         //DE 1 AL 10 CON CON CLICLO INICIADO LA VARIABLE INCREMENTO EN 0
          int i=0; 
          for (i = 0; i < 10; i++) {
              System.out.println(i+1);
          }
          // DEL 1 AL 10 CON CICLO INICIADO LA VARIABLE INCREMENTADO EN 1
          System.out.println("*************************************");
          for (i = 1; i <= 10; i++) {
              System.out.println(i);
          }
      }
}
